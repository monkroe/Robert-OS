export async function checkSession() {
    try {
        const { data: { session }, error } = await db.auth.getSession();
        
        if (error) {
            console.error('Session check error:', error);
            return null;
        }
        
        return session;
        
    } catch (error) {
        console.error('Session check failed:', error);
        return null;
    }
}

